	<h3>Recovery</h3>

	<p>To recover lost details a special mail with encoded details is sent to the address you registered with.</p>

	<hr />

	<h3>Recovery Email</h3>

	<p>The recovery email contains an encrypted link which enables the system to identify you and allow you to
	change your password. 
	<br />
	<hr/>

	<h3>Corrupted Data</h3>	
	<p>If the data that is returned in the email is changed or corrupted then we cannot guarantee that
	this is not a hacking attempt.</p>
	<p>For this reason recovery is aborted.</p>
